# Desafio 06 - Crie um algoritimo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

n1 = int(input('Digite um numero: '))
d = n1*2
t = n1*3
r = n1**2
print('O numero digitado foi {}.' .format(n1))
print('O dobro de {} é {}.' .format(n1, d))
print('O triplo de {} é {}.' .format(n1,t))
print('A raiz quadrada de {} é {}.' .format(n1,r))
